const bundleTitle = document.getElementById("bundleTitle");
const flashMap = document.getElementById("flashMap");
const fileList = document.getElementById("fileList");
const consoleEl = document.getElementById("console");
const marketUrl = document.getElementById("marketUrl");
const installButton = document.getElementById("installButton");
const checkPairing = document.getElementById("checkPairing");
const pairingStatus = document.getElementById("pairingStatus");

const marketplaceUrl = new URL("deck/marketplace-index.json", window.location.href).href;
marketUrl.textContent = marketplaceUrl;

function writeConsole(line) {
  const stamp = new Date().toLocaleTimeString();
  consoleEl.textContent += `\n${stamp} ${line}`;
}

function renderBuild(build) {
  bundleTitle.textContent = `${build.name} ${build.version}`;
  flashMap.innerHTML = "";
  fileList.innerHTML = "";

  for (const item of build.offsets || []) {
    const li = document.createElement("li");
    li.textContent = `0x${Number(item.offset).toString(16).padStart(4, "0")} -> ${item.path}`;
    flashMap.appendChild(li);
  }

  if (!build.files?.length) {
    const empty = document.createElement("div");
    empty.innerHTML = "<strong>Firmware binaries</strong><span>Generated by the ESP-IDF package workflow.</span>";
    fileList.appendChild(empty);
    writeConsole("source-ready bundle loaded; binary files are produced by CI/package step");
    return;
  }

  for (const file of build.files) {
    const card = document.createElement("div");
    card.innerHTML = `<strong>${file.name}</strong><span>${file.length} bytes<br>${file.sha256}</span>`;
    fileList.appendChild(card);
  }
}

async function loadBuild() {
  try {
    const response = await fetch("build.json", { cache: "no-store" });
    if (!response.ok) throw new Error(`${response.status} ${response.statusText}`);
    renderBuild(await response.json());
  } catch (error) {
    bundleTitle.textContent = "Manifest unavailable";
    writeConsole(`build manifest failed: ${error.message}`);
  }
}

installButton?.addEventListener("click", () => {
  writeConsole("installer opened; select the T-Dongle S3 serial/bootloader port");
});

checkPairing?.addEventListener("click", async () => {
  pairingStatus.textContent = "checking http://192.168.4.1/api/status ...";
  try {
    const response = await fetch("http://192.168.4.1/api/status", { cache: "no-store" });
    if (!response.ok) throw new Error(`${response.status} ${response.statusText}`);
    const status = await response.json();
    const paired = status.lifecycle?.paired ? "paired" : "not paired";
    pairingStatus.textContent = [
      `reachable: ${status.name || "CyberDeck-Dongle"} ${status.version || ""}`,
      `pairing: ${paired}`,
      `display: ${status.displayState || "unknown"}`,
      "next: use Begin Pair, confirm on the T-Deck, then Save Profile"
    ].join("\n");
  } catch (error) {
    pairingStatus.textContent = [
      "not reachable",
      "connect to CyberDeck-Link or the dongle network adapter first",
      "open: http://192.168.4.1/",
      `error: ${error.message || "request failed"}`
    ].join("\n");
  }
});

consoleEl.textContent = "installer console ready";
loadBuild();
